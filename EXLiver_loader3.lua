		loader.elements.mainFrame.Position = UDim2.new(
			originalPos.X.Scale, 
			originalPos.X.Offset + math.random(-shake, shake),
			originalPos.Y.Scale,
			originalPos.Y.Offset + math.random(-shake, shake)
		)
		
		-- –ó–∞—Ç–µ–º–Ω–µ–Ω–∏–µ
